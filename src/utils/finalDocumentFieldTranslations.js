/**
 * Get translated text
 * @param {string} key - Translation key
 * @param {string} language - Language code ('en' or 'hi')
 * @returns {string} - Translated text
 */
export function translate(key, language = 'en') {
  const translation = finalDocFieldTranslations[key] || staticTextTranslations[key];
  if (translation && translation[language]) {
    return translation[language];
  }
  return key;
}

/**
 * Detect if text contains Hindi characters
 * @param {string} text - Text to check
 * @returns {boolean} - True if contains Hindi
 */
export function isHindiText(text) {
  if (!text || typeof text !== 'string') return false;
  return /[\u0900-\u097F]/.test(text);
}

/**
 * Detect language from form fields
 * @param {Object} fields - Form fields object
 * @returns {string} - 'hi' or 'en'
 */
export function detectLanguage(fields) {
  if (!fields) return 'en';
  
  const checkFields = [
    fields.vendorName,
    fields.vendeeName,
    fields.vendorAddress,
    fields.vendeeAddress,
    fields.witness1Name,
    fields.witness2Name,
    fields.propertyDescription
  ];
  
  let hindiCount = 0;
  let totalCount = 0;
  
  checkFields.forEach(field => {
    if (field && typeof field === 'string' && field.trim().length > 0) {
      totalCount++;
      if (isHindiText(field)) {
        hindiCount++;
      }
    }
  });
  
  return (totalCount > 0 && hindiCount / totalCount > 0.5) ? 'hi' : 'en';
}
