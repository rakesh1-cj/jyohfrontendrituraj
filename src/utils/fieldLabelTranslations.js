/**
 * Get translated field label
 * @param {string} fieldName - The field name in English
 * @param {string} language - The language code ('en' or 'hi')
 * @returns {string} - The translated field label
 */
export function getFieldLabel(fieldName, language = 'en') {
  // Remove array indices and nested paths
  const cleanFieldName = fieldName
    .replace(/\[\d+\]/g, '')
    .replace(/\.\w+$/, '')
    .split('.')[0];
  
  const translation = fieldLabelTranslations[cleanFieldName];
  
  if (translation && translation[language]) {
    return translation[language];
  }
  
  // Fallback: Format the field name nicely
  return fieldName
    .replace(/([A-Z])/g, ' $1')
    .replace(/\[(\d+)\]/g, ' $1')
    .replace(/\./g, ' - ')
    .replace(/^./, str => str.toUpperCase());
}

/**
 * Check if text contains Hindi characters
 * @param {string} text - The text to check
 * @returns {boolean} - True if text contains Hindi characters
 */
export function containsHindi(text) {
  if (!text || typeof text !== 'string') return false;
  // Devanagari Unicode range: U+0900 to U+097F
  return /[\u0900-\u097F]/.test(text);
}

/**
 * Detect language from form data
 * @param {object} formData - The form data object
 * @returns {string} - 'hi' for Hindi, 'en' for English
 */
export function detectFormLanguage(formData) {
  if (!formData || typeof formData !== 'object') return 'en';
  
  // Check common name fields for Hindi characters
  const checkFields = [
    formData.name,
    formData.sellerName,
    formData.buyerName,
    formData.testatorName,
    formData.address,
    formData.fatherName
  ];
  
  const hindiFieldCount = checkFields.filter(field => containsHindi(field)).length;
  
  // If more than 30% of checked fields contain Hindi, consider it a Hindi form
  return hindiFieldCount > checkFields.length * 0.3 ? 'hi' : 'en';
}
